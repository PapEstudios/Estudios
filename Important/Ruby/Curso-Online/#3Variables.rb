String (Cadena): Representa texto. Puede definirse usando comillas simples o dobles. Ejemplo: 'Hola, mundo', "Ruby es genial".

Symbol (Símbolo): Representa nombres inmutables utilizados como identificadores. Ejemplo: :nombre, :edad.

Boolean (Booleano): Representa valores de verdad, true o false.

Array (Arreglo): Representa una colección ordenada de elementos. Ejemplo: [1, 2, 3], ['rojo', 'verde', 'azul'].

Hash (Hash o Diccionario): Representa una colección de pares clave-valor. Ejemplo: { 'nombre' => 'Juan', 'edad' => 30 }.

NilClass (Clase Nil): Representa la ausencia de un valor. Se utiliza principalmente para indicar la falta de valor en una variable.

Range (Rango): Representa un conjunto de valores secuenciales. Ejemplo: 1..10 (rango inclusivo), 1...10 (rango excluyente).

Regexp (Expresión Regular): Representa patrones de búsqueda en cadenas.

Proc y Lambda: Son objetos que encapsulan bloques de código, permitiendo ser pasados como argumentos y ejecutados posteriormente.

Class (Clase): Representa las clases en Ruby, que son los moldes para la creación de objetos.

Module (Módulo): Representa módulos en Ruby, que permiten la agrupación y el espacio de nombres para métodos y constantes.

File (Archivo): Representa archivos en el sistema de archivos.

IO (Entrada/Salida): Representa las entradas y salidas de datos, como la lectura y escritura en archivos y la entrada/salida estándar.